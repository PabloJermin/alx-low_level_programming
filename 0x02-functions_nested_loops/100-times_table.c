/**
 * print_times_table - printing times table
 * @n: the parameter for the number of times
 * Return: always 0
 */
void print_times_table(int n)
{
	int i,j;

	for (i = 0; i <= n; i++)
		for (j = 0; j = i * n; j++)
		{
			if (n > 15 || n < 0)
			{
			}
			else
			{
				_putchar(j);
			}
		}
}
